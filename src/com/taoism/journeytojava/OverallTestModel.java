public class OverallTestModel {
    String s;
    int i;
    boolean b;

    public OverallTestModel(String s, int i, boolean b) {
        this.s = s;
        this.i = i;
        this.b = b;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public boolean isB() {
        return b;
    }

    public void setB(boolean b) {
        this.b = b;
    }
}
